// Helper functions for crossword automation
export const getCrosswordByDate = (date) => {
  return crosswordPuzzles[date] || null;
};

export const getTodaysCrossword = () => {
  const today = new Date().toISOString().split('T')[0];
  return getCrosswordByDate(today);
};

export const getDifficultyByDay = (dayOfWeek) => {
  const difficultyMap = {
    'Monday': 1,
    'Tuesday': 2, 
    'Wednesday': 3,
    'Thursday': 4,
    'Friday': 5,
    'Saturday': 5,
    'Sunday': 4 // Special themed puzzle
  };
  return difficultyMap[dayOfWeek] || 3;
};

export const generateCrosswordId = (date) => {
  return `crossword-${date}`;
};

// Validation helpers
export const validateCrosswordData = (puzzle) => {
  const required = ['id', 'date', 'grid', 'solution', 'numbers', 'clues'];
  return required.every(field => puzzle.hasOwnProperty(field));
};

export const isValidGridPosition = (row, col, size = 15) => {
  return row >= 0 && row < size && col >= 0 && col < size;
};

// AI automation helpers for Lindy.ai integration
export const exportCrosswordForAI = (puzzle) => {
  return {
    metadata: puzzle.metadata,
    structure: {
      size: puzzle.size,
      difficulty: puzzle.difficulty,
      theme: puzzle.theme
    },
    content: {
      clues: puzzle.clues,
      solution: puzzle.solution
    },
    automation_tags: [
      'crossword',
      'daily_puzzle',
      puzzle.difficultyLabel.toLowerCase().replace('-', '_'),
      puzzle.theme.toLowerCase().replace(/\s+/g, '_')
    ]
  };
};

